def ntbl_bitcnt(x):
    cnt = bits[x & 0x0000000F]
    if x != 0:
        cnt += ntbl_bitcnt(x >> 4)
    return cnt


def btbl_bitcnt(x):
    cnt = bits[x & 0xFF]
    if x != 0:
        cnt += btbl_bitcnt(x >> 8)
    return cnt


